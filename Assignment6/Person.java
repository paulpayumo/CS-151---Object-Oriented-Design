
public class Person 
{
	private String first;
	private String last;
	private int age;
	
	public Person() {}
	
	public void setFirst(String first)
	{
		this.first = first;
	}
	
	public void setLast(String last)
	{
		this.last = last;
	}
	
	public void setAge(int age)
	{
		this.age = age;
	}
	
	public String getFirst()
	{
		return this.first;
	}
	
	public String getLast()
	{
		return this.last;
	}
	
	public int getAge()
	{
		return this.age;
	}
}


